/*
==, ===, !=, !==, >, <, >=, <=
*/

console.log(1 == 1);//true, 1 é igual a 1

console.log(1 == "1");//true, essa comparação não se importa com o tipo

console.log(1 === "1");//false, ja essa precisa alem de ser valores iguais, o tipo tambem precisa ser igual

console.log(1 != 2);//true, 1 é diferente de 2

console.log(1 != "2");//true

console.log(1 !== "2");//true

console.log(3 > 2);//true, 3 é maior que 2

console.log(3 < 6);//true, 3 é menor que 6

console.log(5 >= 4);//true, 5 é maior ou igual a 4

console.log(8 <= 7); // false, 8 é menor do que 7
